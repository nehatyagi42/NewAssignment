package com.skills.model;

public class Skill {
	private String Type;
	private String Id ;
	private String Name;
	public String getType() {
		return Type;
	}
	public void setType(String type) {
		Type = type;
	}
	public String getId() {
		return Id;
	}
	public void setId(String id) {
		Id = id;
	}
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
}
